    private void jButton_PublishModelDirectoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_PublishModelDirectoryActionPerformed

        String globalDir = jTextField_UploadModelDir.getText();
        File[] directories = new File(globalDir).listFiles();

        // File[] directories = new File(globalDir).listFiles(File::isDirectory);
        for (int i = 0; i < directories.length; i++) {

            if (directories[i].isDirectory()) {
                jTextArea_Output.append(i + 1 + ". Saving: " + directories[i].getName() + "\n");
                String individualModelDir = globalDir + directories[i].getName() + "\\";

                // Input the individual model directory and the name of the model directory.
                // the name of the obj and mtl file are the same
                ParseOperation.UploadModel(individualModelDir);
                jTextArea_Output.append("  --- completed --- \n");
            }
        }
        ParseOperation.UpdateCategoryCount();


    }//GEN-LAST:event_jButton_PublishModelDirectoryActionPerformed

