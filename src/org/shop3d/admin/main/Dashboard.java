    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        // TODO add your handling code here:
        ParseOperation.ClearAll("Model");
        ParseOperation.ClearAll("Favorite");
        ParseOperation.ClearAll("Model_UserMade");
        ParseOperation.ClearAll("Order");
        ParseOperation.ClearAll("Order_Item");
        ParseOperation.ClearAll("Sales_Model_Count");
        // Clear the Sales_Order_Count table
        // Get that particular date object.
        ParseQuery<ParseObject> computeQuery = ParseQuery.getQuery("Sales_Order_Count");
        computeQuery.whereNotEqualTo("order_count", 0);

        try {
            List<ParseObject> list = computeQuery.find();
            if (list != null) {
                for (int i = 0; i < list.size(); i++) {
                    list.get(0).put("order_count", 0);
                    list.get(0).put("order_item_count", 0);
                    list.get(0).put("order_item_type_count", 0);
                    list.get(0).put("total_stripe_charge_amount", 0);
                    list.get(0).put("total_profit_amount", 0);
                    list.get(0).put("total_sculpteo_cost_amount", 0);
                    list.get(0).put("total_cc_cost_amount", 0);
                    list.get(0).save();
                }
            }
        } catch (Exception er) {
            jTextArea_Output.append("Error in clearing Sales_Order_Count stuff \n");
        }
        jTextArea_Output.append(" ALL MODEL DATA RESET  \n");
    }//GEN-LAST:event_jButton4ActionPerformed

